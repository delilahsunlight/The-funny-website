function validateAchievements() {
    const invalidIds = ["+", "-", ";", ":"];
    function validateAchievementSet(type: string, achievements: Achievement[]) {
        const set = new Set<string>();
        for (const { uniqueId } of achievements) {
            if (set.has(uniqueId)) {
                throw new Error(`UniqueId "${uniqueId}" found in ${type}`);
            } else {
                if (invalidIds.includes(uniqueId)) {
                    throw new Error(`Not allowed uniqueId "${uniqueId}" found in ${type}`);
                }
                set.add(uniqueId);
            }
        }
    }
    validateAchievementSet('male', ACHIEVEMENTS_MALE)
    validateAchievementSet('female', ACHIEVEMENTS_FEMALE)
}
